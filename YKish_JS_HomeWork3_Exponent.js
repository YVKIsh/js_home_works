function pow(x, y) {
    // змінна, яка зберігає результат
    let result = 1;
    // цикл, який повторюється y разів
    for (let i = 1; i <= y; i++) {
    // множимо результат на x
    result = result * x;
    }
    // повертаємо результат
    return result;
    }
    
    // викликаємо функцію з різними параметрами
    console.log(pow(2, 8)); // поверне 256
    console.log(pow(-3, 3)); // поверне -27
    console.log(pow(4, -2)); // поверне 0.0625, не працює з від'ємними степенями